# Write a Program which takes a statement as input from the user and counts occurrences of each vowel in it.

str1 = input("Enter a String: ")
li = str1.lower().split(" " or "," or ".")
print("Occurrences of 'a' is ", li.count("a"))
print("Occurrences of 'e' is ", li.count("e"))
print("Occurrences of 'i' is ", li.count("i"))
print("Occurrences of 'o' is ", li.count("o"))
print("Occurrences of 'u' is ", li.count("u"))